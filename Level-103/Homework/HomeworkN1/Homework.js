// 3) შეადარეთ ისინი ერთმანეთს ყველა ძირითადი შედარების ოპერატორით (>, <, >=, <=, ==, ===, !=) და შედეგები დაბეჭდეთ კონსოლში.



let numberN1 = 80
let numberN2 = 90

console.log(numberN1 > numberN2);
console.log(numberN1 >= numberN2);
console.log(numberN1 < numberN2);
console.log(numberN1 <= numberN2);
console.log(numberN1 = numberN2);
console.log(numberN1 == numberN2);
console.log(numberN1 === numberN2);
console.log(numberN1 =! numberN2);


